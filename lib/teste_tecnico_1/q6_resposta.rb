"a||=b é um operador de atribuição condicional 'OU/OR'. Esta atribuição simplifica a expressão
'if a
  a = a
else
  a = b', ou seja, se não foi atribuido nenhum valor para a variavel 'a' então ela recebe o que estiver declarado
na variavel 'b'. Outro exemplo 'x||=10', ou seja, se 'x' não tiver recebido nenhum valor anterior, então
'x' recebe o valor 10."